//for of loop

const fruits = ['apple', 'banana', 'cherry'];
for (const fruit of fruits) {
  console.log(fruit);
}
// Output:
// apple
// banana
// cherry


//for in loop


const numbers = [10, 20, 30];
for (const index in numbers) {
  console.log(numbers[index]);
}
// Output:
// 10
// 20
// 30


//for loop
for(i=1;i<=10;i++){
    console.log(i);
    
}

//while loop

let j = 0;
while (j < 5) {
  console.log(j);
  j++;
}
// Output: 0, 1, 2, 3, 4



//do while loop
let a = 0;
do {
  console.log(a);
  a++;
} while (a < 2);
// Output: 0, 1, 2, 3, 4
